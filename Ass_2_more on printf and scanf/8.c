// WAP to input three characters from the user and display characters with their correponding ASCII code

int main(){
    char a,b,c;
    printf("Enter any three character : ");
    scanf("%c %c %c",&a,&b,&c);
    printf("\nThe ASCII code of %c is %d",a,a);
    printf("\nThe ASCII code of %c is %d",b,b);
    printf("\nThe ASCII code of %c is %d",c,c);
    return 0;
}